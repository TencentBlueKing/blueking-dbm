<template>
  <TableEditInput
    ref="editRef"
    v-model="localValue"
    :placeholder="t('请输入IP或从表头批量选择')"
    :rules="rules"
    @input="handleInput" />
</template>

<script lang="ts">
  const instanceAddreddMemo: { [key: string]: Record<string, boolean> } = {};
</script>
<script setup lang="ts">
  import _ from 'lodash';
  import { ref, watch } from 'vue';
  import { useI18n } from 'vue-i18n';

  import { checkMysqlInstances } from '@services/source/instances';

  import { useGlobalBizs } from '@stores';

  import { ipv4 } from '@common/regex';

  import TableEditInput from '@components/render-table/columns/input/index.vue';

  import { random } from '@utils';

  import type { IDataRow } from './Row.vue';

  interface Exposes {
    getValue: () => Promise<{
      old_slave: {
        bk_biz_id: number;
        bk_cloud_id: number;
        ip: string;
        bk_host_id: number;
        port: number;
        instance_address: string;
      };
    }>;
  }

  const instanceKey = `render_source_${random()}`;
  instanceAddreddMemo[instanceKey] = {};

  const { currentBizId } = useGlobalBizs();
  const { t } = useI18n();

  const modelValue = defineModel<IDataRow['oldSlave']>();

  const editRef = ref<InstanceType<typeof TableEditInput>>();
  const localValue = ref('');

  const rules = [
    {
      validator: (value: string) => Boolean(_.trim(value)),
      message: t('目标从库实例不能为空'),
    },
    {
      validator: (value: string) => ipv4.test(value),
      message: t('目标从库实例格式不正确'),
    },
    {
      validator: (value: string) =>
        checkMysqlInstances({
          bizId: currentBizId,
          instance_addresses: [value],
        }).then((data) => {
          if (data.length < 1) {
            return false;
          }
          instanceAddreddMemo[instanceKey] = { [localValue.value]: true };
          const [instanceData] = data;
          if (
            !modelValue.value?.instance_address ||
            modelValue.value.instance_address !== instanceData.instance_address
          ) {
            modelValue.value = {
              bk_cloud_id: instanceData.bk_cloud_id,
              bk_cloud_name: instanceData.bk_cloud_name,
              bk_host_id: instanceData.bk_host_id,
              ip: instanceData.ip,
              port: instanceData.port,
              instance_address: instanceData.instance_address,
              cluster_id: instanceData.cluster_id,
            };
          }
          return true;
        }),
      message: t('目标从库实例不存在'),
    },
    {
      validator: () => {
        const currentClusterSelectMap = instanceAddreddMemo[instanceKey];
        const otherClusterMemoMap = { ...instanceAddreddMemo };
        delete otherClusterMemoMap[instanceKey];

        const otherClusterIdMap = Object.values(otherClusterMemoMap).reduce(
          (result, item) => ({
            ...result,
            ...item,
          }),
          {} as Record<string, boolean>,
        );

        const currentSelectClusterIdList = Object.keys(currentClusterSelectMap);
        for (let i = 0; i < currentSelectClusterIdList.length; i++) {
          if (otherClusterIdMap[currentSelectClusterIdList[i]]) {
            return false;
          }
        }
        return true;
      },
      message: t('源实例重复'),
    },
  ];

  watch(
    () => modelValue.value,
    () => {
      if (modelValue.value) {
        localValue.value = modelValue.value.ip;
        instanceAddreddMemo[instanceKey] = { [localValue.value]: true };
        return;
      }
      instanceAddreddMemo[instanceKey] = {};
    },
    {
      immediate: true,
    },
  );

  const handleInput = () => {
    modelValue.value = undefined;
  };

  onBeforeUnmount(() => {
    delete instanceAddreddMemo[instanceKey];
  });

  defineExpose<Exposes>({
    getValue() {
      return (editRef.value as InstanceType<typeof TableEditInput>)
        .getValue()
        .then(() => {
          if (!modelValue.value) {
            return Promise.reject();
          }
          return {
            old_slave: {
              bk_biz_id: currentBizId,
              bk_cloud_id: modelValue.value.bk_cloud_id,
              ip: modelValue.value.ip,
              bk_host_id: modelValue.value.bk_host_id,
              port: modelValue.value.port,
              instance_address: modelValue.value.instance_address,
            },
          };
        })
        .catch(() =>
          Promise.reject(
            modelValue.value
              ? {
                  old_slave: {
                    bk_biz_id: currentBizId,
                    bk_cloud_id: modelValue.value.bk_cloud_id,
                    ip: modelValue.value.ip,
                    bk_host_id: modelValue.value.bk_host_id,
                    port: modelValue.value.port,
                    instance_address: modelValue.value.instance_address,
                  },
                }
              : undefined,
          ),
        );
    },
  });
</script>
