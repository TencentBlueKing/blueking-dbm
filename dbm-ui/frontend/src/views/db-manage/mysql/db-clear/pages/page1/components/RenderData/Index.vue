<template>
  <RenderTable>
    <template #default>
      <RenderTableHeadColumn
        fixed="left"
        :min-width="300"
        :width="350">
        {{ t('目标集群') }}
        <template #append>
          <BatchOperateIcon
            class="ml-4"
            @click="handleShowBatchSelector" />
        </template>
      </RenderTableHeadColumn>
      <RenderTableHeadColumn
        :min-width="190"
        :width="220">
        <template #append>
          <BatchEditColumn
            v-model="batchEditShow.truncateDataType"
            :data-list="selectList"
            :title="t('清档类型')"
            @change="(value) => handleBatchEditChange(value, 'truncateDataType')">
            <BatchOperateIcon
              class="ml-4"
              type="edit"
              @click="handleBatchEditShow('truncateDataType')" />
          </BatchEditColumn>
        </template>
        {{ t('清档类型') }}
      </RenderTableHeadColumn>
      <RenderTableHeadColumn
        :min-width="200"
        :width="250">
        {{ t('目标DB名') }}
        <template #append>
          <BatchEditColumn
            v-model="batchEditShow.dbPatterns"
            :title="t('目标DB名')"
            type="taginput"
            @change="(value) => handleBatchEditChange(value, 'dbPatterns')">
            <BatchOperateIcon
              class="ml-4"
              type="edit"
              @click="handleBatchEditShow('dbPatterns')" />
          </BatchEditColumn>
        </template>
      </RenderTableHeadColumn>
      <RenderTableHeadColumn
        :min-width="200"
        :required="false"
        :width="250">
        {{ t('忽略DB名') }}
        <template #append>
          <BatchEditColumn
            v-model="batchEditShow.ignoreDbs"
            :title="t('忽略DB名')"
            type="taginput"
            @change="(value) => handleBatchEditChange(value, 'ignoreDbs')">
            <BatchOperateIcon
              class="ml-4"
              type="edit"
              @click="handleBatchEditShow('ignoreDbs')" />
          </BatchEditColumn>
        </template>
      </RenderTableHeadColumn>
      <RenderTableHeadColumn
        :min-width="200"
        :width="250">
        {{ t('目标表名') }}
        <template #append>
          <BatchEditColumn
            v-model="batchEditShow.tablePatterns"
            :title="t('目标表名')"
            type="taginput"
            @change="(value) => handleBatchEditChange(value, 'tablePatterns')">
            <BatchOperateIcon
              class="ml-4"
              type="edit"
              @click="handleBatchEditShow('tablePatterns')" />
          </BatchEditColumn>
        </template>
      </RenderTableHeadColumn>
      <RenderTableHeadColumn
        :min-width="200"
        :required="false"
        :width="250">
        {{ t('忽略表名') }}
        <template #append>
          <BatchEditColumn
            v-model="batchEditShow.ignoreTables"
            :title="t('忽略表名')"
            type="taginput"
            @change="(value) => handleBatchEditChange(value, 'ignoreTables')">
            <BatchOperateIcon
              class="ml-4"
              type="edit"
              @click="handleBatchEditShow('ignoreTables')" />
          </BatchEditColumn>
        </template>
      </RenderTableHeadColumn>
      <RenderTableHeadColumn
        fixed="right"
        :required="false"
        :width="100">
        {{ t('操作') }}
      </RenderTableHeadColumn>
    </template>

    <template #data>
      <slot />
    </template>
  </RenderTable>
</template>
<script setup lang="ts">
  import { useI18n } from 'vue-i18n';

  import BatchEditColumn from '@components/batch-edit-column/Index.vue';
  import BatchOperateIcon from '@components/batch-operate-icon/Index.vue';
  import RenderTableHeadColumn from '@components/render-table/HeadColumn.vue';
  import RenderTable from '@components/render-table/Index.vue';

  import type { IDataRowBatchKey } from './Row.vue';

  interface Emits {
    (e: 'batchSelectCluster'): void;
    (e: 'batchEdit', value: string | string[], filed: IDataRowBatchKey): void;
  }

  const emits = defineEmits<Emits>();

  const { t } = useI18n();

  const batchEditShow = reactive({
    truncateDataType: false,
    dbPatterns: false,
    tablePatterns: false,
    ignoreDbs: false,
    ignoreTables: false,
  });

  const selectList = [
    {
      value: 'truncate_table',
      label: t('清除表数据_truncatetable'),
    },
    {
      value: 'drop_table',
      label: t('清除表数据和结构_droptable'),
    },
    {
      value: 'drop_database',
      label: t('删除整库_dropdatabase'),
    },
  ];

  const handleBatchEditShow = (key: IDataRowBatchKey) => {
    batchEditShow[key] = !batchEditShow[key];
  };

  const handleBatchEditChange = (value: string | string[], filed: IDataRowBatchKey) => {
    emits('batchEdit', value, filed);
  };

  const handleShowBatchSelector = () => {
    emits('batchSelectCluster');
  };
</script>
